#include<iostream>
using namespace std;
int main(){
    int firstNumber,secondNumber,result;
    cout<<"Enter the first integer number:"<<"\n";
    cin>>firstNumber;
    cout<<"Enter the second integer number:"<<"\n";
    cin>>secondNumber;
    result=firstNumber+secondNumber;
    cout<<"The first  integer number is "<<firstNumber<<"\n";
    cout<<"The second integer number is "<<secondNumber<<"\n";
    cout<<"The sum of  both   number is "<<result<<"\n";
    return 0;
}